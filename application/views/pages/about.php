<p class="c1"><span><h2>SwiftSharing, The Story.</h2></span></p>

<p class="c0"><span></span></p>

<p class="c1"><span>To really see how SwiftSharing was started, we have to venture back to the Summer of 2010, a little. Paul Henry hired Alaxic Smith for the summer to do some design work with his Content Management System he uses for school events.They began working in June, when Alaxic noticed something. People were liking phrases on an external website through a Facebook like plugin. After discussing it with Paul, they decided since it was so simple that they&rsquo;d create one. LikeBench was then born. The LikeBench was a project that grew really big, really fast. <br>Eventually LikeBench&rsquo;s domain was blocked by Facebook, and the traffic decreased rapidly. </span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>Alaxic and Paul then went back to the drawing board on what they would spend the rest of the summer creating, YMO Life (Yours, Mine, and Our Life). YMO Life was based off of a framework called jCow. It was a very vague idea, and was just to test the social networking waters. The website got a total of 33 members. Paul then left the company to pursue other web education projects. The website was scratched in mid-November.</span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>Around the same time, Alaxic met Magan Tyler, a then sophomore at Longview High School, on Facebook. Alaxic sent her a friend request, and started talking to her. Magan made some simple logos for SwiftSharing, and really got into the site, after he said he wanted to build the next Facebook, and better. She gave alot of input on the site, and told him, if he needed her that she was here. They then got together at school during lunch. On Magan&rsquo;s MacBook they noticed a Facebook trend: Sending your friends colors and answering questions about themselves. Alaxic thought of a categorization idea which put your friends posts into default categories to eliminate the messiness. The idea was then deemed SwiftSharing. Swift meaning fast, and sharing is the sharing of what you&#39;re doing on a social network. Development began later that month.</span>
</p>
	
<p class="c1"><span>Alaxic was doing all the development of the site, while contacting Paul for minor help with issues. Alaxic&rsquo;s friend, Spencer Smith, was brought in to mainly managing how we would promote the site once it&#39;s released and the business end of the company. Magan had not spoke with Alaxic until the week prior to the release which was January 11th, 2011. This date formed five consecutive ones. Though development of the site was going rather well, Alaxic did need some minor help, and contacted Paul again. </span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>Launch night finally came, and it was time for SwiftSharing to make it&#39;s debut. The website gained 40 members within it&#39;s first night. The first major milestone was set when the site gained 1,000 members within 2 weeks of release. That&#39;s when Paul took another definite active role in the company, due to exponential growth. A week after, Magan joined the SwiftSharing Team. </span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>Neil Parikh, was contacted through a former developer of SwiftSharing. Neil is a business major and has worked on a project that was going to receive a $50,000 investment, but the CEO caused the entire deal to fail. He is the current SwiftSharing Chief Financial Officer. </span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>In May, The SwiftSharing Team established official Co-Founders of the site, who is Paul Henry, Magan Tyler, and Alaxic Smith. </span>
</p>

<p class="c0"><span></span></p>

<p class="c1"><span>Spencer took an unexpected leave, and eventually was terminated from the company. </span></p>

<p class="c0"><span></span></p>

<p class="c1"><span>Paul and Alaxic then took an unexpected leave, in mid-June, to early July. Magan stayed with SwiftSharing, until getting a hold of Paul, and asking him to re-join the team. Alaxic came back unwillingly, but by that time Paul and Magan had talked about removing Alaxic as Chief Executive Officer. Alaxic then had to step down, and Magan became CEO. After Alaxic was unwilling to work, Paul and Magan then removed him from the team. Alaxic is however still a Co-Founder of SwiftSharing. </span>
</p>

<p class="c1">
	<span>Currently, The SwiftSharing Team is preparing the site for it&rsquo;s out of beta stage. &nbsp; </span></p>

